"""Tests for the route analysis tool.

These tests do not exercise any HTTP path — the tool reads local GPX files only.
The fixtures live in tests/fixtures/routes and are regenerated by running
`uv run python tests/fixtures/routes/_generate.py`.
"""

import json
from pathlib import Path

from intervals_icu_mcp.tools.routes import _characterise_segment, analyze_route_climbs

FIXTURES = Path(__file__).parent / "fixtures" / "routes"


def _load(result: str) -> dict:
    return json.loads(result)


class TestFlatRoute:
    """A near-flat route should yield zero climbs."""

    async def test_no_climbs_detected(self):
        result = await analyze_route_climbs(gpx_path=str(FIXTURES / "flat_route.gpx"))
        r = _load(result)
        assert "data" in r
        assert r["data"]["climbs"] == []
        assert r["data"]["route_summary"]["total_distance_km"] > 9.0
        assert r["analysis"]["eligible_climb_count"] == 0


class TestSingleClimb:
    """2 km flat + 1.6 km @ 6% + 3 km descent should yield one Cat-3 climb."""

    async def test_one_climb_detected_with_expected_shape(self):
        result = await analyze_route_climbs(gpx_path=str(FIXTURES / "single_climb.gpx"))
        r = _load(result)
        climbs = r["data"]["climbs"]
        assert len(climbs) == 1
        climb = climbs[0]
        # Climb should start near 2 km and have length close to 1.6 km.
        assert 1.8 <= climb["start_km"] <= 2.2
        assert 1500 <= climb["length_m"] <= 1700
        # Gradient should be close to 6%.
        assert 5.5 <= climb["avg_gradient_percent"] <= 6.5
        # Eligible for hard effort at cycling thresholds.
        assert climb["eligible_for_hard_effort"] is True
        # Should fall in the threshold/V02 zone for a ~7-10 min climb.
        assert climb["suggested_zone"] in ("Z4 threshold", "Z5 VO2max")

    async def test_recovery_segments_cover_flat_and_descent(self):
        result = await analyze_route_climbs(gpx_path=str(FIXTURES / "single_climb.gpx"))
        r = _load(result)
        segments = r["data"]["recovery_segments"]
        # Expect one flat segment before the climb and one descent after.
        characters = [s["character"] for s in segments]
        assert "flat" in characters
        assert "descent" in characters
        # Each segment carries a hard_effort_recommended flag.
        for seg in segments:
            assert "hard_effort_recommended" in seg
        # Descents are never recommended for hard efforts.
        for seg in segments:
            if seg["character"] in ("descent", "shallow_descent"):
                assert seg["hard_effort_recommended"] is False



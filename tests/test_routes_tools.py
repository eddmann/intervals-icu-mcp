class TestCustomThresholds:
    """Overrides should change what gets detected."""

    async def test_raising_min_gradient_filters_out_climbs(self):
        # The multi-climb route's third climb is exactly 4%; raising threshold to 5% drops it.
        result = await analyze_route_climbs(
            gpx_path=str(FIXTURES / "multi_climb.gpx"),
            min_gradient_percent=5.0,
        )
        r = _load(result)
        climbs = r["data"]["climbs"]
        # First two climbs (7.7% kicker, 5.5% middle) survive; the 4% climb is filtered.
        assert len(climbs) == 2
        assert all(c["avg_gradient_percent"] >= 5.0 for c in climbs)

    async def test_metadata_reports_thresholds_used(self):
        result = await analyze_route_climbs(
            gpx_path=str(FIXTURES / "single_climb.gpx"),
            min_gradient_percent=2.0,
            min_length_m=100.0,
        )
        r = _load(result)
        assert r["metadata"]["thresholds"]["min_gradient_percent"] == 2.0
        assert r["metadata"]["thresholds"]["min_length_m"] == 100.0



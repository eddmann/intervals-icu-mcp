class TestGpxContent:
    """The gpx_content parameter lets Claude analyze user-attached files
    without needing them on the MCP server's filesystem."""

    async def test_content_produces_same_result_as_path(self):
        content = (FIXTURES / "single_climb.gpx").read_text()
        by_path = _load(await analyze_route_climbs(gpx_path=str(FIXTURES / "single_climb.gpx")))
        by_content = _load(await analyze_route_climbs(gpx_content=content))
        # Same number of climbs and same key shape values from both paths.
        assert len(by_path["data"]["climbs"]) == len(by_content["data"]["climbs"])
        assert by_path["data"]["climbs"][0]["length_m"] == by_content["data"]["climbs"][0]["length_m"]
        # Source label differs.
        assert by_path["metadata"]["source"] != "inline_content"
        assert by_content["metadata"]["source"] == "inline_content"

    async def test_invalid_xml_content_returns_error(self):
        result = await analyze_route_climbs(gpx_content="<not-gpx>nope</not-gpx>")
        r = _load(result)
        assert "error" in r
        assert r["error"]["type"] == "route_parse_error"

    async def test_providing_both_path_and_content_returns_error(self):
        content = (FIXTURES / "single_climb.gpx").read_text()
        result = await analyze_route_climbs(
            gpx_path=str(FIXTURES / "single_climb.gpx"),
            gpx_content=content,
        )
        r = _load(result)
        assert "error" in r
        assert r["error"]["type"] == "validation_error"

    async def test_providing_neither_returns_error(self):
        result = await analyze_route_climbs()
        r = _load(result)
        assert "error" in r
        assert r["error"]["type"] == "validation_error"


class TestSegmentCharacterisation:
    """The 5-bucket gradient classifier."""

    def test_descent(self):
        assert _characterise_segment(-3.0, climb_threshold_pct=3.0) == ("descent", False)
        assert _characterise_segment(-1.5, climb_threshold_pct=3.0) == ("descent", False)

    def test_shallow_descent(self):
        assert _characterise_segment(-1.0, climb_threshold_pct=3.0) == ("shallow_descent", False)
        assert _characterise_segment(-0.5, climb_threshold_pct=3.0) == ("shallow_descent", False)

    def test_flat(self):
        assert _characterise_segment(0.0, climb_threshold_pct=3.0) == ("flat", False)
        assert _characterise_segment(0.4, climb_threshold_pct=3.0) == ("flat", False)

    def test_shallow_rise_is_hard_effort_eligible(self):
        # Between the "flat" upper bound and the climb threshold.
        assert _characterise_segment(0.5, climb_threshold_pct=3.0) == ("shallow_rise", True)
        assert _characterise_segment(2.5, climb_threshold_pct=3.0) == ("shallow_rise", True)

    def test_running_profile_uses_its_own_threshold(self):
        # At running's 4% threshold, a 3.5% segment is shallow_rise, not a missed climb.
        assert _characterise_segment(3.5, climb_threshold_pct=4.0) == ("shallow_rise", True)


